function sisonke_supported_languages(): array
{
    return [
        'en' => ['label' => 'English', 'short' => 'EN', 'html' => 'en-ZA'],
        'zu' => ['label' => 'isiZulu', 'short' => 'ZU', 'html' => 'zu-ZA'],
        'xh' => ['label' => 'isiXhosa', 'short' => 'XH', 'html' => 'xh-ZA'],
        'st' => ['label' => 'Sesotho', 'short' => 'ST', 'html' => 'st-ZA'],
        'af' => ['label' => 'Afrikaans', 'short' => 'AF', 'html' => 'af-ZA'],
    ];
}

function sisonke_normalize_language(mixed $language): string
{
    $language = strtolower(trim((string) $language));
    return array_key_exists($language, sisonke_supported_languages()) ? $language : 'en';
}

function sisonke_current_language(): string
{
    if (session_status() !== PHP_SESSION_ACTIVE) {
        session_start();
    }

    if (isset($_GET['lang'])) {
        $_SESSION['sisonke_lang'] = sisonke_normalize_language($_GET['lang']);
    }

    return sisonke_normalize_language($_SESSION['sisonke_lang'] ?? 'en');
}

function sisonke_html_language(): string
{
    $language = sisonke_current_language();
    $languages = sisonke_supported_languages();

    return $languages[$language]['html'] ?? 'en-ZA';
}

function sisonke_language_url(string $language): string
{
    $language = sisonke_normalize_language($language);
    $requestUri = (string) ($_SERVER['REQUEST_URI'] ?? '');
    $parts = parse_url($requestUri);
    $path = is_array($parts) && isset($parts['path']) ? (string) $parts['path'] : $requestUri;
    $query = [];

    if (is_array($parts) && isset($parts['query'])) {
        parse_str((string) $parts['query'], $query);
    }

    $query['lang'] = $language;
    return $path . '?' . http_build_query($query);
}

