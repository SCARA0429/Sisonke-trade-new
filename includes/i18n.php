function sisonke_t(string $key, array $params = []): string
{
    $translations = sisonke_translations();
    $language = sisonke_current_language();
    $text = $translations[$language][$key] ?? $translations['en'][$key] ?? $key;

    foreach ($params as $name => $value) {
        $text = str_replace('{' . $name . '}', (string) $value, $text);
    }

    return $text;
}

